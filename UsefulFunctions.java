
public class UsefulFunctions {
    public static char findFile(int nfile) {
        char file = 'a';
        switch (nfile) {
        case 0: {
            file = 'a';
            break;
        }
        case 1: {
            file = 'b';
            break;
        }
        case 2: {
            file = 'c';
            break;
        }
        case 3: {
            file = 'd';
            break;
        }
        case 4: {
            file = 'e';
            break;
        }
        case 5: {
            file = 'f';
            break;
        }
        case 6: {
            file = 'g';
            break;
        }
        case 7: {
            file = 'h';
            break;
        }
        case 8: {
            file = 'i';
            break;
        }
        case 9: {
            file = 'j';
            break;
        }
        }
        return file;
    }

}
